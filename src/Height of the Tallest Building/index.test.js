import tallestBuildingHeight from './index';

test('tallestBuildingHeight', () => {
  expect(
    tallestBuildingHeight([
      '         ',
      ' ##      ',
      ' ##      ',
      '###   ## ',
      '###   ## ',
      '###   ###',
      '###   ###',
    ]),
  ).toBe('120m');
  expect(
    tallestBuildingHeight([
      '            ##',
      '            ##',
      '            ##',
      '###   ###   ##',
      '###   ###   ###',
      '###   ###   ###',
      '###   ###   ###',
    ]),
  ).toBe('140m');
  expect(
    tallestBuildingHeight([
      '               ',
      '               ',
      '               ',
      '       #    ###',
      '      # #   ###',
      '###   ###   ###',
      '###   ###   ###',
    ]),
  ).toBe('80m');
  expect(
    tallestBuildingHeight([
      '               ',
      '               ',
      '               ',
      '               ',
      '               ',
      '###   ###   ###',
      '###   ###   ###',
    ]),
  ).toBe('40m');
  expect(
    tallestBuildingHeight([
      '                  ',
      ' #                ',
      ' #     #          ',
      '###    #    ###   ',
      '###   # #   ###   #',
      '###   ###   ###   #',
      '###   ###   ###   #',
    ]),
  ).toBe('120m');
  expect(
    tallestBuildingHeight([
      '          ',
      '###   # # ',
      '###   ### ',
      '###   ### ',
    ]),
  ).toBe('60m');
  expect(
    tallestBuildingHeight([
      '###                    #####  ',
      '###      #             #####  ',
      '###     ###            #####  ',
      '######  ###            #######',
      '######  ######  ###    #######',
      '###################    #######',
      '###############################',
      '###############################',
    ]),
  ).toBe('160m');
  expect(
    tallestBuildingHeight([
      '                              ',
      '                              ',
      '                              ',
      '###                    #####  ',
      '###      #             #####  ',
      '###     ###            #####  ',
      '######  ###            #######',
      '######  ######  ###    #######',
      '###################    #######',
      '###############################',
      '###############################',
    ]),
  ).toBe('160m');
  expect(
    tallestBuildingHeight([
      '                              ',
      '                         ###  ',
      '                         ###  ',
      '###                    #####  ',
      '###      #             #####  ',
      '###     ###            #####  ',
      '######  ###            #######',
      '######  ######  ###    #######',
      '###################    #######',
      '###############################',
      '###############################',
    ]),
  ).toBe('200m');
});
