import yahtzeeScoreCalc from './index';

test('yahtzeeScoreCalc', () => {
  expect(
    yahtzeeScoreCalc([
      [3, 3, 2, 6, 4],
      [1, 1, 2, 5, 5],
      [4, 4, 4, 1, 6],
      [6, 5, 2, 2, 5],
      [2, 1, 4, 4, 2],
      [6, 2, 6, 2, 6],
      [6, 6, 4, 6, 1],
      [4, 1, 5, 2, 2],
      [5, 5, 5, 5, 5],
      [2, 3, 4, 5, 1],
      [2, 3, 4, 4, 6],
      [4, 4, 1, 4, 4],
      [3, 2, 6, 1, 5],
    ]),
  ).toBe(90);
  expect(
    yahtzeeScoreCalc([
      [2, 6, 2, 6, 4],
      [3, 2, 2, 2, 2],
      [1, 4, 5, 5, 4],
      [4, 2, 4, 4, 6],
      [2, 2, 5, 5, 5],
      [6, 6, 6, 6, 6],
      [3, 3, 2, 1, 3],
      [4, 1, 4, 4, 4],
      [1, 1, 1, 3, 3],
      [6, 1, 4, 5, 1],
      [2, 3, 4, 5, 6],
      [1, 1, 5, 1, 1],
      [5, 3, 4, 6, 6],
    ]),
  ).toBe(218);
  expect(
    yahtzeeScoreCalc([
      [1, 3, 1, 1, 2],
      [1, 2, 4, 5, 6],
      [6, 3, 4, 3, 4],
      [3, 1, 1, 4, 4],
      [5, 5, 5, 5, 3],
      [6, 2, 6, 6, 6],
      [1, 4, 4, 2, 1],
      [3, 3, 3, 3, 3],
      [2, 2, 1, 1, 2],
      [6, 1, 2, 3, 4],
      [2, 3, 5, 4, 1],
      [4, 4, 4, 4, 4],
      [3, 3, 4, 5, 6],
    ]),
  ).toBe(279);
});
