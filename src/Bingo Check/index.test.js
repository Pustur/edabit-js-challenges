import bingoCheck from './index';

test('bingoCheck', () => {
  expect(
    bingoCheck([
      [45, 'x', 31, 74, 87],
      [64, 'x', 47, 32, 90],
      [37, 'x', 68, 83, 54],
      [67, 'x', 98, 39, 44],
      [21, 'x', 24, 30, 52],
    ]),
  ).toBe(true);
  expect(
    bingoCheck([
      ['x', 43, 31, 74, 87],
      [64, 'x', 47, 32, 90],
      [37, 65, 'x', 83, 54],
      [67, 98, 39, 'x', 44],
      [21, 59, 24, 30, 'x'],
    ]),
  ).toBe(true);
  expect(
    bingoCheck([
      [37, 16, 84, 51, 33],
      [64, 12, 47, 32, 90],
      ['x', 'x', 'x', 'x', 'x'],
      [67, 19, 98, 39, 44],
      [21, 75, 24, 30, 52],
    ]),
  ).toBe(true);
  expect(
    bingoCheck([
      [45, 'x', 31, 74, 87],
      [64, 78, 'x', 'x', 90],
      [37, 'x', 68, 'x', 54],
      [67, 'x', 98, 'x', 'x'],
      [21, 'x', 24, 30, 52],
    ]),
  ).toBe(false);
  expect(
    bingoCheck([
      [45, 46, 31, 74, 'x'],
      [64, 78, 80, 'x', 90],
      [37, 81, 'x', 55, 54],
      [67, 'x', 98, 34, 77],
      ['x', 33, 24, 30, 52],
    ]),
  ).toBe(true);
});
