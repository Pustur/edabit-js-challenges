import routeTracer from './index';

test('routeTracer', () => {
  expect(
    routeTracer(
      [
        ['-', '#', '-', '-'],
        ['$', '#', '@', '&'],
        ['-', '#', '$', '&'],
        ['$', '-', '&', '-'],
      ],
      'lddr',
    ),
  ).toEqual({
    route: [
      ['-', '#', '-', '-'],
      ['$', 'x', '@', '&'],
      ['-', 'x', '$', '&'],
      ['$', 'x', 'x', '-'],
    ],
    items: '##&',
  });
  expect(
    routeTracer(
      [
        ['-', '-', '&', '-'],
        ['-', '-', '&', '-'],
        ['$', '#', '-', '#'],
        ['-', '@', '-', '$'],
      ],
      'ulurrr',
    ),
  ).toEqual({
    route: [
      ['-', '-', '&', '-'],
      ['x', 'x', 'x', 'x'],
      ['x', 'x', '-', '#'],
      ['-', '@', '-', '$'],
    ],
    items: '#$&',
  });
  expect(
    routeTracer(
      [
        ['-', '#', '&', '-'],
        ['-', '#', '-', '$'],
        ['@', '#', '&', '&'],
        ['-', '$', '-', '$'],
      ],
      'druulurrrd',
    ),
  ).toEqual({
    route: [
      ['x', 'x', 'x', 'x'],
      ['x', 'x', '-', 'x'],
      ['@', 'x', '&', '&'],
      ['x', 'x', '-', '$'],
    ],
    items: '$###&$',
  });
});
